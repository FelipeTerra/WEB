// Aula Operações

var x, y, z;

// Operadores aritméticos

x = 20;
y = 3;
z = x + y;
console.log(z);
z = x - y;
console.log(z);
z = x * y;
console.log(z);
z = x / y;
console.log(z);

// Operadores Bit a Bit

x = 20;
y = 1;
z = x & y;
console.log(z);
z = x | y;
console.log(z);
z = x ^ y;
console.log(z);

y = 0xFFFF0000;
z =  ~ y;
console.log(z);

x = 10;
z = x >> 2;
console.log(z);